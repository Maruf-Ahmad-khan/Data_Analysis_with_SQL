CREATE VIEW  PRODUCTO  AS
SELECT 
    PRODUCTID, PRODUCTNAME
FROM
    PRODUCTS
WHERE
    PRICE > (SELECT 
            AVG(SUPPLIERID)
        FROM
            PRODUCTS);
