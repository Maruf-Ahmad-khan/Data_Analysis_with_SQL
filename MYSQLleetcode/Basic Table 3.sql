USE DEMO;
SELECT * FROM DEPOSIT;
CREATE VIEW EMPLOYEEDATA AS
    SELECT 
        DEPOSIT.ACTNO, DEPOSIT.CNAME, BRANCH.CITY
    FROM
        DEPOSIT
            INNER JOIN
        BRANCH ON DEPOSIT.BNAME = BRANCH.BNAME;
SELECT * FROM employeedata;

